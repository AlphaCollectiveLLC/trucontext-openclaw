/**
 * Uninstall flow вЂ” non-destructive, graph data preserved
 *
 * Each step is wrapped independently вЂ” a failure in one step does not
 * abort the rest. All results are collected and reported at the end.
 */

import fs from 'fs';
import { readState, statePath } from './state.js';
import { removeFragment } from './inject.js';
import { discoverAgents } from './discover.js';
import { unregisterCron } from './cron.js';
import { removeSkill } from './skill.js';
import { log, confirm } from './utils.js';

export async function uninstall({ args = [] } = {}) {
  const force = args.includes('--force');

  log.info('\nв•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—');
  log.info('в•‘       trucontext-openclaw вЂ” Uninstall              в•‘');
  log.info('в•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ\n');

  log.info('This will:\n');
  log.info('  вЂў Remove the tc-memory skill from your workspace');
  log.info('  вЂў Remove TruContext fenced blocks from each agent\'s AGENTS.md');
  log.info('  вЂў Unregister the daily cron job');
  log.info('  вЂў Delete the local state file\n');
  log.info('  Your TruContext graph data is PRESERVED.');
  log.info('  To delete: trucontext roots delete <id>\n');

  if (!force) {
    const proceed = await confirm('Continue? [y/N] ');
    if (!proceed) { log.info('\nUninstall cancelled.'); process.exit(0); }
  }

  const state = readState();
  const errors = [];

  // в”Ђв”Ђ Step 1: Remove AGENTS.md fragments в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  log.info('\nв”Ђв”Ђ Removing AGENTS.md fragments в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ');
  try {
    for (const agent of discoverAgents()) {
      if (!fs.existsSync(agent.agentsPath)) continue;
      try {
        const { removed } = removeFragment(agent.agentsPath);
        if (removed) {
          log.info(`  вњ“ ${agent.name} (${agent.id})`);
        } else {
          log.info(`  в†’ ${agent.name} (${agent.id}): no managed block found, skipping`);
        }
      } catch (err) {
        log.warn(`  вњ— ${agent.name} (${agent.id}): ${err.message}`);
        errors.push(`AGENTS.md remove failed for ${agent.id}: ${err.message}`);
      }
    }
  } catch (err) {
    log.warn(`  вњ— Could not discover agents: ${err.message}`);
    errors.push(`Agent discovery failed: ${err.message}`);
  }

  // в”Ђв”Ђ Step 2: Remove skill в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  log.info('\nв”Ђв”Ђ Removing trucontext-openclaw skill в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ');
  try {
    await removeSkill(state.workspace_root);
    log.info('  вњ“ Skill removed from ~/.openclaw/skills');
  } catch (err) {
    log.warn(`  вњ— Could not remove skill: ${err.message}`);
    errors.push(`Skill removal failed: ${err.message}`);
  }

  // в”Ђв”Ђ Step 3: Unregister cron в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  log.info('\nв”Ђв”Ђ Unregistering cron в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ');
  try {
    unregisterCron();
    log.info('  вњ“ Cron removed');
  } catch (err) {
    log.warn(`  вњ— Could not unregister cron: ${err.message}`);
    errors.push(`Cron unregister failed: ${err.message}`);
  }

  // в”Ђв”Ђ Step 4: Delete state file в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  log.info('\nв”Ђв”Ђ Deleting state file в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ');
  try {
    const sp = statePath();
    if (fs.existsSync(sp)) {
      fs.unlinkSync(sp);
      log.info(`  вњ“ Deleted: ${sp}`);
    } else {
      log.info('  в†’ State file not found, skipping');
    }
  } catch (err) {
    log.warn(`  вњ— Could not delete state file: ${err.message}`);
    errors.push(`State file deletion failed: ${err.message}`);
  }

  // в”Ђв”Ђ Summary в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  if (errors.length > 0) {
    log.info('\nв•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—');
    log.info('в•‘   вљ  Uninstall completed with warnings              в•‘');
    log.info('в•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ\n');
    log.warn('The following steps had errors:');
    for (const e of errors) log.warn(`  вЂў ${e}`);
    log.info('\nYour TC graph data is untouched at trucontext.ai\n');
  } else {
    log.info('\nв•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—');
    log.info('в•‘   вњ“ TruContext removed from OpenClaw               в•‘');
    log.info('в•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ\n');
    log.info('Your TC graph data is untouched at trucontext.ai\n');
  }
}
